// Action Creators
export function addRank(newRank) {
  return { type: ADD, newRank };
}

// Reducer
export default function reducer(state = initialState, action = {}) {
  switch (action.type) {
    case 'rank/ADD':
      let sortedList = [
        ...state.list,
        action.newRank
      ]

      sortedList.sort((a, b) => b.score - a.score)
      sortedList = sortedList.map((data, idx) => {
        return {
          ...data,
          rank: idx + 1
        }
      })

      return {
        list: sortedList
      }

    default: return state;
  }
}
